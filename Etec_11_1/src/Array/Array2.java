
package Array;

public class Array2 {

 
    public static void main(String[] args) {
       String name[][]= {{"Sok","Dara"},{"Long","Heng"}};//2 row and 2 column
       for(int i=0;i<2;i++){
           for(int j=0;j<2;j++){
               System.out.print(name[i][j]+" ");
           }
           System.out.println("\n");
       }
       //foreach loop
  
    }
    
}
