
package Introduction;


public class Example2 {
    public static void main(String[] args) {
        int num1=20;
        int num2=10;
        int result;
                
        result=num1+num2;
        System.out.println("Number1 + Number2 = "+result);
        result=num1-num2;
        System.out.println("Number1 - Number2 = "+result);
        result=num1*num2;
        System.out.println("Number1 x Number2 = "+result);
        result=num1/num2;
        System.out.println("Number1 / Number2 = "+result);
        result=num1%num2;
        System.out.println("Number1 % Number2 = "+result);
  
    }
    
}
